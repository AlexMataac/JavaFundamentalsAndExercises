package javaFundamentals;

public class VariablesAndDataTypes {
    public static void main(String[] args) {

        char section = 'A';
        String name = "Alex";
        boolean isPresent = true;
        int num3 = 5;
        double grade1 = 99.33;
        float grade2 = 99.33f;

        //concatenation
        System.out.println("I am " + name);
        System.out.println("I am at Section " + section);
        System.out.println("I am present today " + isPresent);
        System.out.println("I am " + num3 + " years old");
        System.out.println("My average grade is " + grade1);


    }
}

